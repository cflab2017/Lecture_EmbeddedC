#include <reg52.h>

/*
 * 08강 예제 1 — 버튼 입력을 읽어 LED 제어.
 * 대상: AT89C52, 컴파일러: Keil C51.
 *
 * 버튼은 P3.2 에 액티브 로우(누르면 0, 풀업으로 평소 1)로 연결.
 * LED 는 P1.0 에 액티브 로우(0 이면 ON).
 *
 * 8051 의 포트는 '준쌍방향'이라, 입력으로 읽기 전에 그 핀에 1 을 먼저
 * 써 두어야 외부 신호를 제대로 읽을 수 있다.
 */

sbit BTN = P3 ^ 2;     /* 입력: 버튼 */
sbit LED = P1 ^ 0;     /* 출력: LED */

void main(void)
{
    BTN = 1;           /* 입력 준비: 읽기 전에 1 을 써 둔다 */

    while (1) {
        if (BTN == 0) {    /* 버튼 눌림(0) */
            LED = 0;       /* LED ON */
        } else {           /* 버튼 뗌(1) */
            LED = 1;       /* LED OFF */
        }
    }
}
