#include <reg52.h>

/*
 * 정답 1 (C51, AT89C52) — 버튼으로 LED 8개 한꺼번에 켜기
 *
 * 핵심 포인트:
 *  - 입력 핀(P3.3)은 읽기 전에 1 을 써서 입력 준비를 한다.
 *  - 버튼이 눌리면(0) P1 전체를 0x00(모두 ON), 뗌(1)이면 0xFF(모두 OFF).
 *
 * 흔한 실수:
 *  - 입력 핀에 1 을 안 써서 항상 0 으로 읽히는 경우(준쌍방향 포트 함정).
 *  - 액티브 로우를 헷갈려 동작이 반대가 되는 경우.
 *
 * 예상 동작(시뮬레이터): P3.3 을 0 으로 만들면(버튼 누름) P1 의 LED 8개가
 *  모두 ON, 1 로 두면 모두 OFF.
 */

sbit BTN = P3 ^ 3;

void main(void)
{
    BTN = 1;                 /* 입력 준비 */

    while (1) {
        if (BTN == 0) {      /* 눌림 */
            P1 = 0x00;       /* 액티브 로우: 모두 ON */
        } else {
            P1 = 0xFF;       /* 모두 OFF */
        }
    }
}
