#include <reg52.h>

/*
 * 06강 예제 1 — SFR(특수 기능 레지스터)에 접근하는 두 가지 방법.
 * 대상: AT89C52 (8051 호환), 컴파일러: Keil C51.
 *
 *  (1) 포트 전체에 한 번에 쓰기:  P1 = 0x0F;
 *  (2) 개별 비트에 이름을 붙여 쓰기: sbit 로 선언 후 LED0 = 0;
 *
 * reg52.h 안에서 P1 은 'sfr P1 = 0x90;' 으로 미리 선언돼 있어,
 * 우리는 P1 이라는 이름으로 0x90 번지의 포트 SFR 에 바로 접근한다.
 */

/* P1 포트의 개별 비트에 의미 있는 이름을 붙인다 (P1 은 비트 주소 가능 SFR). */
sbit LED0 = P1 ^ 0;    /* P1.0 */
sbit LED7 = P1 ^ 7;    /* P1.7 */

void main(void)
{
    /* (1) 포트 전체 쓰기: P1.0~P1.3 = 1, 나머지 = 0 */
    P1 = 0x0F;

    /* (2) 개별 비트 쓰기: 나머지 비트는 그대로 두고 한 비트만 */
    LED0 = 0;          /* P1.0 = 0 → 0x0E */
    LED7 = 1;          /* P1.7 = 1 → 0x8E */

    /* 임베디드 main 은 종료하지 않는다. P1 값을 유지하며 대기. */
    while (1) {
        /* 시뮬레이터 Port 1 창에서 P1 = 0x8E 를 확인한다. */
    }
}
